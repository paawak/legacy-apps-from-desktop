    private void initHelp(String helpType){
        txtPnHelp.setContentType("text/html");
        try{
        PropertyFileUtils pt = new PropertyFileUtils("props/Display");
        txtPnHelp.setPage(getClass().getResource(pt.getVal(helpType)));
        txtPnHelp.addHyperlinkListener(createHyperLinkListener());
        }catch(Exception e){
            System.out.println(e);
        }
    }
    
    public HyperlinkListener createHyperLinkListener() {
        return new HyperlinkListener() {
            public void hyperlinkUpdate(HyperlinkEvent e) {
                if (e.getEventType() == HyperlinkEvent.EventType.ACTIVATED) {
                    if (e instanceof HTMLFrameHyperlinkEvent) {
                        ((HTMLDocument)txtPnHelp.getDocument()).processHTMLFrameHyperlinkEvent(
                        (HTMLFrameHyperlinkEvent)e);
                    } else {
                        try {
                            txtPnHelp.setPage(e.getURL());
                        } catch (IOException ioe) {
                            System.out.println("IOE: " + ioe);
                        }
                    }
                }
            }
        };
    }
    
